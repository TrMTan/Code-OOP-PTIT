    private void jComboBox4ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox4ItemStateChanged
        // TODO add your handling code here:
        if(jComboBox4.getSelectedIndex()== 1){
            listBCC.sort(new Comparator<BangChamCong>(){
                @Override
                public int compare(BangChamCong o1, BangChamCong o2) {
                    String[] ht1 = o1.getCongnhan().getHt().split("\\s+");
                    String[] ht2 = o2.getCongnhan().getHt().split("\\s+");
                    String ten1 = ht1[ht1.length - 1];
                    String ten2 = ht2[ht2.length - 1];
                    if(ten1.equalsIgnoreCase(ten2)){
                        return o1.getCongnhan().getHt().compareToIgnoreCase(o2.getCongnhan().getHt());
                    }else{
                        return ten1.compareToIgnoreCase(ten2);
                    }
                }
            });
        }
        if(jComboBox4.getSelectedIndex()== 2){
            listBCC.sort(new Comparator<BangChamCong>(){
                @Override
                public int compare(BangChamCong o1, BangChamCong o2) {
                    return Integer.compare(o1.getSoNgay(), o2.getSoNgay());
                }
            });
        }
        tmBCC.setRowCount(0);
        for(BangChamCong i : listBCC){
            tmBCC.addRow(i.toObjects());
        }
    }//GEN-LAST:event_jComboBox4ItemStateChanged

