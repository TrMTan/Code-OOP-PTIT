    public void load2Ma(){
        jComboBox2.removeAllItems();
        jComboBox3.removeAllItems();
        listCN = IOFile.doc(fCN);
        listX = IOFile.doc(fX);
        for(CongNhan i : listCN){
            jComboBox2.addItem(i.getMa() + "");
        }
        for(Xuong i : listX){
            jComboBox3.addItem(i.getMa() + "");
        }
    }
    
    private int getSoNgay(int maCN){
        int so = 0;
        for(BangChamCong i : listBCC){
            if(i.getCongnhan().getMa() == maCN){
                so += i.getSoNgay();
            }
        }
        return so;
    }
    
    private CongNhan getCongNhanByMa(int ma){
        for(CongNhan i : listCN){
            if(i.getMa() == ma){
                return i;
            }
        }     
        return null;
    }
    
    private Xuong getXuongByMa(int ma){
        for(Xuong i : listX){
            if(i.getMa() == ma){
                return i;
            }
        }     
        return null;
    }

