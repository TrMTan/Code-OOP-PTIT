    private void btThem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThem2ActionPerformed
        // TODO add your handling code here:
        int maoto = Integer.parseInt(jComboBox2.getSelectedItem().toString());
        int makh = Integer.parseInt(jComboBox3.getSelectedItem().toString());
        if(trung2Ma(makh, maoto)){
            JOptionPane.showMessageDialog(this, "Trung 2 Ma!");
            jComboBox2.requestFocus();
            return;
        }
        int sl = 0, soNgayT;
        int slxe = getOToByMa(maoto).getSl();
        try {
            sl = Integer.parseInt(jTextField7.getText());
            if(sl > slxe){
                JOptionPane.showMessageDialog(this, "Khong Du Xe");
                jTextField1.requestFocus();
                return;
            }
            soNgayT = Integer.parseInt(jTextField8.getText());
            BangSX bsx = new BangSX(getOToByMa(maoto), getKhachHangByMa(makh), sl, soNgayT);
            tmBSX.addRow(bsx.toObjects());
            listBSX.add(bsx);
            getOToByMa(maoto).setSl(slxe - sl);
            IOFile.viet(fOto, listOTo);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nhap So!");
            jTextField7.requestFocus();
            return;
        }
    }//GEN-LAST:event_btThem2ActionPerformed

