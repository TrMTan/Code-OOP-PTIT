    private void jRadioButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton1ActionPerformed
        // TODO add your handling code here:
        if(jRadioButton1.isSelected()){
            listBSX.sort(new Comparator<BangSX>(){
                @Override
                public int compare(BangSX o1, BangSX o2) {
                    String[] ht1 = o1.getKhachhang().getHt().split("\\s+");
                    String[] ht2 = o2.getKhachhang().getHt().split("\\s+");
                    String ten1 = ht1[ht1.length - 1];
                    String ten2 = ht2[ht2.length - 1];
                    if(ten1.equalsIgnoreCase(ten2)){
                        return o1.getKhachhang().getHt().compareToIgnoreCase(o2.getKhachhang().getHt());
                    }else{
                        return ten1.compareToIgnoreCase(ten2);
                    }
                }
            });
        }
        showBSX(listBSX);
    }//GEN-LAST:event_jRadioButton1ActionPerformed

