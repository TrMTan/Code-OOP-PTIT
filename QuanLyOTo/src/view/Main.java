    private boolean trung2Ma(int maKH, int maOTo){
        for(BangSX i : listBSX){
            if(i.getOto().getMa() == maOTo && i.getKhachhang().getMa() == maKH){
                return true;
            }
        }
        return false;
    }
    
    private OTo getOToByMa(int ma){
        for(OTo i : listOTo){
            if(i.getMa() == ma){
                return i;
            }
        }     
        return null;
    }
    
