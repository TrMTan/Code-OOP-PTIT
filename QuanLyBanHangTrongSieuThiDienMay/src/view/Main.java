    private void btSua2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSua2ActionPerformed
        // TODO add your handling code here:
        int r = jTable3.getSelectedRow();
        if(r >= 0 && r < jTable3.getRowCount()){
            String ngayb = jTextField7.getText();
            int sl = Integer.parseInt(jTextField8.getText());
            tmBDSB.setValueAt(ngayb, r, 5);
            tmBDSB.setValueAt(sl, r, 6);
            int mamh = Integer.parseInt(jComboBox2.getSelectedItem().toString());
            int manv = Integer.parseInt(jComboBox3.getSelectedItem().toString());
            BangDSB bdsb = new BangDSB(getMatHangByMa(mamh), getNhanVienByMa(manv), ngayb, sl);
            listBDSB.set(r, bdsb);
        }else{
            JOptionPane.showMessageDialog(this, "Chon Dich Vu De Sua");
        }
    }//GEN-LAST:event_btSua2ActionPerformed

    private void jRadioButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton1ActionPerformed
        // TODO add your handling code here:
        if(jRadioButton1.isSelected()){
            listBDSB.sort(new Comparator<BangDSB>(){
                @Override
                public int compare(BangDSB o1, BangDSB o2) {
                    String[] ht1 = o1.getNv().getHt().split("\\s+");
                    String[] ht2 = o2.getNv().getHt().split("\\s+");
                    String ten1 = ht1[ht1.length - 1];
                    String ten2 = ht2[ht2.length - 1];
                    if(ten1.equalsIgnoreCase(ten2)){
                        return o1.getNv().getHt().compareToIgnoreCase(o2.getNv().getHt());
                    }else{
                        return ten1.compareToIgnoreCase(ten2);
                    }
                }
            });
        }
        showBDSB(listBDSB);
    }//GEN-LAST:event_jRadioButton1ActionPerformed

