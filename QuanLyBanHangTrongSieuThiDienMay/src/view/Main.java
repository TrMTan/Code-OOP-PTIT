    private void btThem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThem2ActionPerformed
        // TODO add your handling code here:
        int mamh = Integer.parseInt(jComboBox2.getSelectedItem().toString());
        int manv = Integer.parseInt(jComboBox3.getSelectedItem().toString());
        if(trung2Ma(manv, mamh)){
            JOptionPane.showMessageDialog(this, "Trung 2 Ma!");
            jComboBox2.requestFocus();
            return;
        }
        String ngayb;
        int sl = 0;
        int slmh = getMatHangByMa(mamh).getSl();
        try {
            ngayb = jTextField7.getText();
            sl = Integer.parseInt(jTextField8.getText());
            if(sl > slmh){
                JOptionPane.showMessageDialog(this, "Khong Du Hang");
                jTextField1.requestFocus();
                return;
            }
            BangDSB bdsb = new BangDSB(getMatHangByMa(mamh), getNhanVienByMa(manv), ngayb, sl);
            tmBDSB.addRow(bdsb.toObjects());
            listBDSB.add(bdsb);
            getMatHangByMa(mamh).setSl(slmh - sl);
            IOFile.viet(fMH, listMH);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nhap So!");
            jTextField7.requestFocus();
            return;
        }
    }//GEN-LAST:event_btThem2ActionPerformed

