    public void loadMH(){
        File f = new File(fMH);
        if(f.exists()){
            listMH = IOFile.doc(fMH);
        }else{
            listMH = new ArrayList<>();
        }
    }
    
    public void showMH(List<MatHang> list){
        tmMH.setRowCount(0);
        for(MatHang i : list){
            tmMH.addRow(i.toObjects());
        }
    }
    
