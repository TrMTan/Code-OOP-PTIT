    public void loadKH(){
        File f = new File(fKH);
        if(f.exists()){
            listKH = IOFile.doc(fKH);
        }else{
            listKH = new ArrayList<>();
        }
    }
    
    public void showKH(List<KhachHang> list){
        tmKH.setRowCount(0);
        for(KhachHang i : list){
            tmKH.addRow(i.toObjects());
        }
    }
    
