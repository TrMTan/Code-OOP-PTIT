    private void jComboBox5ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox5ItemStateChanged
        // TODO add your handling code here:
        if(jComboBox5.getSelectedIndex()== 1){
            listSTK.sort(new Comparator<STK>(){
                @Override
                public int compare(STK o1, STK o2) {
                    String[] ht1 = o1.getKh().getHt().split("\\s+");
                    String[] ht2 = o2.getKh().getHt().split("\\s+");
                    String ten1 = ht1[ht1.length - 1];
                    String ten2 = ht2[ht2.length - 1];
                    if(ten1.equalsIgnoreCase(ten2)){
                        return o1.getKh().getHt().compareToIgnoreCase(o2.getKh().getHt());
                    }else{
                        return ten1.compareToIgnoreCase(ten2);
                    }
                }
            });
        }
        if(jComboBox5.getSelectedIndex()== 2){
            listSTK.sort(new Comparator<STK>(){
                @Override
                public int compare(STK o1, STK o2) {
                    return Integer.compare(o1.getTiengui(), o2.getTiengui());
                }
            });
        }
        tmSTK.setRowCount(0);
        for(STK i : listSTK){
            tmSTK.addRow(i.toObjects());
        }
    }//GEN-LAST:event_jComboBox5ItemStateChanged

